public class pesertaModel {
    
    private String kodePeserta;
    private String namaPeserta;
    private String jkPeserta;
    private String alamatPeserta;
    private String teleponPeserta;
    
    public String getKodePeserta(){
        return this.kodePeserta;
    }
    public void setKodePeserta(String kodePeserta){
        this.kodePeserta = kodePeserta;
    }
    
    public String getNamaPeserta(){
        return this.namaPeserta;
    }
    public void setNamaPeserta(String namaPeserta){
        this.namaPeserta = namaPeserta;
    }
    
    public String getJkPeserta(){
        return this.jkPeserta;
    }
    public void setJkPeserta(String jkPeserta){
        this.jkPeserta = jkPeserta;
    }
    
    public String getAlamatPeserta(){
        return this.alamatPeserta;
    }
    public void setAlamatPeserta(String alamatPeserta){
        this.alamatPeserta = alamatPeserta;
    }
    
    public String getTeleponPeserta(){
        return this.teleponPeserta;
    }
    public void setTeleponPeserta(String teleponPeserta){
        this.teleponPeserta = teleponPeserta;
    }
 
}
